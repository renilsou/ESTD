numero = int(input("Digite um número entre 1 e 7: "))
if numero == 1:
    print(f"Dia da semana correspondente ao número {numero}: Segunda-feira")
elif numero == 2:
    print(f"Dia da semana correspondente ao número {numero}: Terça-feira")
elif numero == 3:
    print(f"Dia da semana correspondente ao número {numero}: Quarta-feira")
elif numero == 4:
    print(f"Dia da semana correspondente ao número {numero}: Quinta-feira")
elif numero == 5:
    print(f"Dia da semana correspondente ao número {numero}: Sexta-feira")
elif numero == 6:
    print(f"Dia da semana correspondente ao número {numero}: Sábado")
else:
    print(f"O número {numero} não corresponde a nenhum dia da semana")